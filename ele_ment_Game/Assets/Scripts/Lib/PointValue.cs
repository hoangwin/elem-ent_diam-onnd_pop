
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;



   public class PointValue
    {
        public int x;
        public int y;
        public int value; 
        public PointValue()
        {
            x = 0;
            y = 0;
            value = 0;
        }
        public PointValue(int _x, int _y,int _value)
        {
            x = _x;
            y = _y;
            value = _value;
        }
    }


